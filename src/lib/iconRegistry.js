/** Names available in the admin icon picker. */
export const ICON_NAMES = Object.keys(ICON_REGISTRY);

/** Resolve an icon name to a component, falling back to a sensible default. */
export function resolveIcon(name, fallback = Heart) {
  return (name && ICON_REGISTRY[name]) || fallback;
}
